/**
 * 状态模式 (State Pattern)
 *
 * ━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━
 * 一、意图
 * ━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━
 * 允许一个对象在其内部状态改变时改变它的行为，
 * 看起来就像改变了它的类。
 *
 * 核心思想：把每种状态的行为封装到独立的状态类中，
 *          对象持有当前状态的引用，行为委托给当前状态来执行。
 *          消灭那些遍布各处的 if (state == "xxx") 判断。
 *
 * ━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━
 * 二、UML 结构
 * ━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━
 *
 *    ┌───────────────────────┐       ┌──────────────────┐
 *    │ Order (上下文)         │       │ OrderState(接口)  │
 *    │ - state: OrderState   │──────►│ + pay()           │
 *    │ + pay()               │       │ + ship()          │
 *    │ + ship()              │       │ + receive()       │
 *    │ + cancel()            │       │ + cancel()        │
 *    └───────────────────────┘       └────────┬─────────┘
 *                                             │
 *                          ┌──────────────────┼──────────────────┐
 *                          │                  │                  │
 *                     NewState          PaidState          ShippedState ...
 *
 * ━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━
 * 三、状态模式 vs 策略模式
 * ━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━
 * 策略模式：客户端主动选择策略，策略之间无关联
 * 状态模式：状态自动流转，状态之间有明确的转换规则
 *
 * ━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━
 * 四、本示例 —— 订单状态机
 * ━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━
 * 订单状态流转：
 *   新建 ──pay()──► 已支付 ──ship()──► 已发货 ──receive()──► 已完成
 *    │                │                                       │
 *    └──cancel()──► 已取消 ◄────────cancel()──────────────────┘
 *                      (已发货后不能取消)
 */
