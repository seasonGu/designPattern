/**
 * 观察者模式 (Observer Pattern)
 *
 * ━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━
 * 一、意图
 * ━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━
 * 定义对象之间的一对多依赖关系，当一个对象（主题）的状态发生改变时，
 * 所有依赖于它的对象（观察者）都会自动收到通知并更新。
 *
 * 核心思想：解耦「事件的发生者」和「事件的处理者」。
 *          发布者不需要知道谁在监听，只管发布事件。
 *
 * ━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━
 * 二、UML 结构
 * ━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━
 *
 *  ┌──────────────────────────┐       ┌────────────────────────┐
 *  │  Subject (主题/被观察者)  │       │  Observer (观察者接口)   │
 *  │  + addObserver()         │──────►│  + onEvent(event)       │
 *  │  + removeObserver()      │  1:N  └───────────┬────────────┘
 *  │  + notifyObservers()     │                   │
 *  └──────────────────────────┘        ┌──────────┼──────────┐
 *                                      │          │          │
 *                                   短信通知   邮件通知   库存服务
 *
 * ━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━
 * 三、适用场景
 * ━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━
 * 1. 事件驱动系统（GUI事件、消息队列）
 * 2. 状态变化需要通知多个模块（订单状态、配置变更）
 * 3. 发布-订阅场景
 *
 * ━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━
 * 四、Spring 中的观察者模式
 * ━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━
 * Spring 的事件机制就是观察者模式的实现：
 *   - ApplicationEvent       → 事件（消息）
 *   - ApplicationListener    → 观察者
 *   - ApplicationEventPublisher → 主题（发布者）
 *   - @EventListener 注解     → 更简洁的观察者声明方式
 *
 * ━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━
 * 五、本示例场景 —— 订单状态变更通知
 * ━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━
 * 当订单状态变化（已支付、已发货、已完成）时，
 * 需要同时通知：短信服务、邮件服务、库存服务等多个模块。
 * 这些模块彼此独立，互不依赖。
 */
