/**
 * 责任链模式 (Chain of Responsibility Pattern)
 *
 * ━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━
 * 一、意图
 * ━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━
 * 使多个对象都有机会处理请求，避免请求的发送者和接收者之间的耦合。
 * 将这些对象连成一条链，沿着链传递请求，直到有对象处理它为止。
 *
 * 核心思想：请求沿链传递，每个节点决定「处理」还是「传给下一个」。
 *          发送者不需要知道谁会处理，处理者也不需要知道请求来自哪里。
 *
 * ━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━
 * 二、UML 结构
 * ━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━
 *
 *   请求 ──► ┌─────────┐ ──► ┌─────────┐ ──► ┌─────────┐
 *            │ 组长     │     │ 经理     │     │ 总监     │
 *            │ ≤1天     │     │ ≤3天     │     │ ≤7天     │
 *            └─────────┘     └─────────┘     └─────────┘
 *             能处理？             能处理？          能处理？
 *             是→处理             是→处理           是→处理
 *             否→传递→            否→传递→          否→拒绝
 *
 * ━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━
 * 三、适用场景
 * ━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━
 * 1. 审批流程（请假、报销、采购）
 * 2. 过滤器链（Servlet Filter、Spring Interceptor）
 * 3. 异常处理（try-catch 链）
 * 4. 日志级别处理（DEBUG → INFO → WARN → ERROR）
 *
 * ━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━
 * 四、Spring / Java 中的责任链
 * ━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━
 * - Servlet FilterChain：多个 Filter 依次处理请求
 * - Spring HandlerInterceptor：preHandle → 控制器 → postHandle
 * - Spring Security 过滤器链：认证 → 授权 → CSRF → ...
 *
 * ━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━
 * 五、本示例场景 —— 请假审批系统
 * ━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━
 * 员工请假：
 *   ≤1天  → 组长审批
 *   ≤3天  → 经理审批
 *   ≤7天  → 总监审批
 *   >7天  → 需要 CEO 审批
 */
