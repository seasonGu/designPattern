/**
 * 备忘录模式 (Memento Pattern)
 *
 * ━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━
 * 一、意图
 * ━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━
 * 在不破坏封装性的前提下，捕获一个对象的内部状态，
 * 并在该对象之外保存这个状态，以便以后恢复。
 *
 * 核心思想：「快照」—— 拍张照片存起来，随时可以恢复到拍照时的样子。
 *          对象自己负责创建和恢复快照，外部只负责保管，不能偷看内容。
 *
 * ━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━
 * 二、UML 结构
 * ━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━
 *
 *  ┌──────────────────┐    ┌────────────────────┐    ┌──────────────────┐
 *  │ GameSaveManager  │    │ GameCharacter      │    │ CharacterMemento │
 *  │ (管理者/Caretaker)│    │ (原发器/Originator) │    │ (备忘录/Memento)  │
 *  │                  │    │                    │    │                  │
 *  │ saves: Stack     │    │ + save(): Memento  │───►│ hp, mp, level... │
 *  │ + saveGame()     │    │ + restore(Memento) │◄───│ (不可变快照)      │
 *  │ + loadGame()     │    └────────────────────┘    └──────────────────┘
 *  └──────────────────┘
 *   只负责保管，不能                自己创建快照
 *   查看备忘录内容                  自己从快照恢复
 *
 * ━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━
 * 三、备忘录模式 vs 命令模式
 * ━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━
 * 命令模式的 undo：通过「反向操作」撤销（如删除的反向是插入）
 * 备忘录模式的 undo：通过「恢复快照」撤销（直接回到之前的状态）
 * 命令模式更节省内存，备忘录模式更简单可靠。
 *
 * ━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━
 * 四、本示例 —— 游戏存档
 * ━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━
 * 游戏角色在战斗中不断变化（HP、MP、等级、位置），
 * 玩家可以随时存档（创建备忘录），打 Boss 失败后读档恢复。
 */
