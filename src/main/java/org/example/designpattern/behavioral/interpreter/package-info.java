/**
 * 解释器模式 (Interpreter Pattern)
 *
 * ━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━
 * 一、意图
 * ━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━
 * 给定一种语言，定义它的文法的一种表示，
 * 并定义一个解释器，用来解释该语言中的句子。
 *
 * 核心思想：把语法规则抽象成类的层次结构（语法树），
 *          每个节点负责解释自己的部分。
 *
 * ━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━
 * 二、UML 结构
 * ━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━
 *
 *  ┌──────────────────────┐
 *  │ Expression (抽象表达式) │
 *  │ + interpret(): int    │
 *  └──────────┬───────────┘
 *             │
 *   ┌─────────┼──────────────┐
 *   │         │              │
 * Number   AddExpr      MultiplyExpr
 * (终结符)  (非终结符)    (非终结符)
 *          left + right  left * right
 *
 *  表达式 "3 + 5 * 2" 的语法树：
 *         Add
 *        /   \
 *     Num(3)  Mul
 *            /   \
 *         Num(5) Num(2)
 *
 * ━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━
 * 三、适用场景
 * ━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━
 * 1. 简单的 DSL（领域特定语言）
 * 2. 规则引擎（如折扣规则、权限规则）
 * 3. SQL 解析、正则表达式、数学表达式
 * 4. 文法简单且效率要求不高的场景
 *
 * ━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━
 * 四、本示例 —— 数学表达式解析器
 * ━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━
 * 解析并计算简单数学表达式：支持 +、-、*，正确处理运算优先级。
 * "3 + 5 * 2 - 1" → 构建语法树 → 递归解释 → 结果 12
 */
