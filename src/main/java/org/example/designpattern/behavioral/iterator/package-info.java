/**
 * 迭代器模式 (Iterator Pattern)
 *
 * ━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━
 * 一、意图
 * ━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━
 * 提供一种方法顺序访问一个聚合对象中的各个元素，
 * 而又不暴露该对象的内部表示。
 *
 * 核心思想：不管集合内部用数组、链表、树还是哈希表，
 *          客户端都用同一种方式遍历: while(hasNext()) next()
 *
 * ━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━
 * 二、UML 结构
 * ━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━
 *
 *  ┌───────────────────┐        ┌──────────────────┐
 *  │ CourseCollection  │        │ CourseIterator    │
 *  │ (聚合接口)         │        │ (迭代器接口)      │
 *  │ + createIterator()│───────►│ + hasNext()      │
 *  └────────┬──────────┘        │ + next()         │
 *           │                   └────────┬─────────┘
 *  ┌────────┴──────────┐       ┌────────┴─────────┐
 *  │ CompulsoryCourses │       │ 对应的Iterator     │
 *  │ (数组实现)         │       │ (知道如何遍历数组)  │
 *  ├───────────────────┤       ├──────────────────┤
 *  │ ElectiveCourses   │       │ 对应的Iterator     │
 *  │ (List实现)         │       │ (知道如何遍历List) │
 *  └───────────────────┘       └──────────────────┘
 *
 * ━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━
 * 三、Java 内置的迭代器
 * ━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━
 * java.util.Iterator       —— 标准迭代器接口
 * java.lang.Iterable       —— 实现后可用 for-each 语法
 * java.util.stream.Stream  —— 函数式遍历（内部迭代器）
 *
 * ━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━
 * 四、本示例 —— 课程管理系统
 * ━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━
 * 必修课集合（数组实现）和选修课集合（List实现），
 * 内部数据结构不同，但客户端用统一的迭代器遍历，无需关心底层实现。
 */
