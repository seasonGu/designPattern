/**
 * 策略模式 (Strategy Pattern)
 *
 * ━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━
 * 一、意图
 * ━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━
 * 定义一系列算法，将它们各自封装起来，并且使它们可以互相替换。
 * 策略模式让算法独立于使用它的客户端而变化。
 *
 * 核心思想：把「变化的行为」抽成接口，通过组合而非继承来切换行为。
 *          消灭 if-else / switch 判断不同算法的代码。
 *
 * ━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━
 * 二、UML 结构
 * ━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━
 *
 *    ┌────────────────────────┐
 *    │  Context (上下文)       │        ┌────────────────────┐
 *    │  - strategy: Strategy  │───────►│  Strategy (策略接口) │
 *    │  + pay()               │        │  + pay(amount)      │
 *    └────────────────────────┘        └─────────┬──────────┘
 *                                                │
 *                                 ┌──────────────┼──────────────┐
 *                                 │              │              │
 *                           ┌─────┴─────┐ ┌─────┴─────┐ ┌─────┴─────┐
 *                           │  AliPay   │ │ WechatPay │ │ CreditCard│
 *                           └───────────┘ └───────────┘ └───────────┘
 *
 * ━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━
 * 三、适用场景
 * ━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━
 * 1. 多种算法/行为需要动态切换（支付方式、排序算法、折扣计算）
 * 2. 想避免大量 if-else / switch 来选择不同行为
 * 3. 算法需要独立演化，互不影响
 *
 * ━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━
 * 四、与工厂方法的区别
 * ━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━
 * 工厂方法：关注「创建什么对象」
 * 策略模式：关注「使用什么算法/行为」
 * 两者经常配合：用工厂来创建策略对象。
 *
 * ━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━
 * 五、本示例场景 —— 电商支付系统
 * ━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━
 * 用户下单后可以选择不同支付方式（支付宝、微信、信用卡），
 * 每种支付方式的处理逻辑不同，用策略模式来封装。
 */
