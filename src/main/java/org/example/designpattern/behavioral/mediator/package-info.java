/**
 * 中介者模式 (Mediator Pattern)
 *
 * ━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━
 * 一、意图
 * ━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━
 * 用一个中介对象来封装一系列对象的交互，
 * 使各对象不需要显式地相互引用，从而降低耦合。
 *
 * 核心思想：把「网状通信」变成「星状通信」。
 *          N个对象之间两两通信 = N*(N-1)/2 条关系
 *          引入中介者后 = N 条关系（每个对象只认识中介者）
 *
 * ━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━
 * 二、UML 结构
 * ━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━
 *
 *   ┌────────────────┐       ┌──────────────────────┐
 *   │ ChatRoom       │       │ User (抽象同事)       │
 *   │ (中介者)        │◄──────│ - mediator           │
 *   │ + sendMessage() │       │ + send()             │
 *   │ + addUser()     │       │ + receive()          │
 *   └────────────────┘       └──────────┬───────────┘
 *                                       │
 *                            ┌──────────┴───────────┐
 *                        NormalUser            AdminUser
 *
 *   不用中介者：              用中介者：
 *     A ←→ B                  A ──┐
 *     A ←→ C                  B ──┤── ChatRoom
 *     B ←→ C                  C ──┘
 *   (网状，每人认识每人)      (星状，每人只认识中介者)
 *
 * ━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━
 * 三、适用场景
 * ━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━
 * 1. 多个对象之间存在复杂的交互关系（网状依赖）
 * 2. 想要复用单个对象，但它与很多其他对象耦合
 * 3. 聊天室、MVC 中的 Controller、消息队列
 *
 * ━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━
 * 四、中介者模式 vs 观察者模式
 * ━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━
 * 观察者：一对多广播，Subject 不关心谁在监听
 * 中介者：多对多协调，Mediator 知道所有参与者并控制交互逻辑
 *
 * ━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━
 * 五、本示例 —— 聊天室
 * ━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━
 * 多个用户通过聊天室（中介者）发送消息：
 *   - 普通用户发消息 → 中介者转发给其他所有人
 *   - 管理员发消息 → 中介者以公告形式广播
 *   - 私聊 → 中介者定向转发
 *   用户之间完全解耦，互相不持有引用。
 */
