/**
 * 访问者模式 (Visitor Pattern)
 *
 * ━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━
 * 一、意图
 * ━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━
 * 表示一个作用于某对象结构中的各元素的操作。
 * 它使你可以在不改变各元素类的前提下定义作用于这些元素的新操作。
 *
 * 核心思想：「双重分派」(Double Dispatch)
 *   element.accept(visitor)  → visitor.visit(element)
 *   第一次分派：根据 element 的实际类型调用对应的 accept
 *   第二次分派：根据 visitor 的实际类型调用对应的 visit
 *
 * ━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━
 * 二、UML 结构
 * ━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━
 *
 *  ┌─────────────┐        ┌──────────────────┐
 *  │ Goods       │        │ GoodsVisitor     │
 *  │ (元素接口)   │        │ (访问者接口)      │
 *  │ + accept(v) │───────►│ + visit(Book)    │
 *  └──────┬──────┘        │ + visit(Fruit)   │
 *         │               │ + visit(Elec.)   │
 *  ┌──────┼──────┐        └──────┬───────────┘
 *  │      │      │               │
 * Book  Fruit  Elec.     ┌───────┼───────────┐
 *                     Discount  Packaging  ExportReport
 *
 * ━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━
 * 三、访问者模式 vs 策略模式
 * ━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━
 * 策略模式：一个对象的一种行为有多种实现（一对多）
 * 访问者模式：多种对象的多种操作的组合（多对多）
 *
 * ━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━
 * 四、适用 / 不适用
 * ━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━
 * 适用：元素类型稳定，但经常需要新增操作
 * 不适用：元素类型经常变化（每加一种元素，所有 Visitor 都要改）
 *
 * ━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━
 * 五、本示例 —— 电商商品计价系统
 * ━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━
 * 商品类型（Book、Fruit、Electronics）固定不变，
 * 但需要不断新增操作（折扣计算、包装方案、出口报表...），
 * 每种操作对不同商品有不同逻辑 —— 完美契合访问者模式。
 */
