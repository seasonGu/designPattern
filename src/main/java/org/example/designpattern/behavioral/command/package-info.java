/**
 * 命令模式 (Command Pattern)
 *
 * ━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━
 * 一、意图
 * ━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━
 * 将一个请求封装为一个对象，从而可以用不同的请求对客户进行参数化，
 * 支持请求的排队、记录日志、以及撤销/重做操作。
 *
 * 核心思想：把「动作」变成「对象」。
 *          动作可以存储、传递、排队、撤销。
 *
 * ━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━
 * 二、UML 结构
 * ━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━
 *
 *   ┌────────────────────┐      ┌────────────────────┐
 *   │ Invoker (调用者)    │─────►│ Command (命令接口)  │
 *   │ TextEditor         │      │ + execute()        │
 *   │ + executeCommand() │      │ + undo()           │
 *   │ + undo()           │      └─────────┬──────────┘
 *   │ + redo()           │                │
 *   └────────────────────┘     ┌──────────┼──────────┐
 *                              │          │          │
 *                         InsertCmd   DeleteCmd  ReplaceCmd
 *                              │
 *                              ▼
 *                     ┌─────────────────┐
 *                     │ Receiver(接收者) │
 *                     │ TextDocument    │
 *                     └─────────────────┘
 *
 * ━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━
 * 三、适用场景
 * ━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━
 * 1. 需要撤销/重做功能（编辑器、画图工具）
 * 2. 需要记录操作日志（审计、回放）
 * 3. 需要延迟执行或排队执行（任务队列、线程池）
 * 4. 需要组合多个操作为一个宏命令
 *
 * ━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━
 * 四、本示例场景 —— 文本编辑器
 * ━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━
 * 模拟一个支持插入、删除、替换的文本编辑器，
 * 每个操作都是一个命令对象，支持 Undo 和 Redo。
 */
