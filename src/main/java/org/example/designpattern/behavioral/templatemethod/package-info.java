/**
 * 模板方法模式 (Template Method Pattern)
 *
 * ━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━
 * 一、意图
 * ━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━
 * 在父类中定义一个算法的骨架（模板），将某些步骤的具体实现延迟到子类。
 * 子类可以在不改变算法结构的前提下，重新定义某些步骤。
 *
 * 核心思想：「流程固定，细节可变」。
 *          父类控制整体流程（不可覆盖），子类填充具体步骤。
 *
 * ━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━
 * 二、UML 结构
 * ━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━
 *
 *    ┌──────────────────────────────────┐
 *    │  AbstractDataExporter (抽象类)    │
 *    │                                  │
 *    │  + export()          ◄── 模板方法（final，定义流程）
 *    │  # connect()         ◄── 抽象步骤（子类必须实现）
 *    │  # fetchData()       ◄── 抽象步骤
 *    │  # formatData()      ◄── 抽象步骤
 *    │  # writeFile()       ◄── 抽象步骤
 *    │  # cleanup()         ◄── 钩子方法（子类可选覆盖）
 *    └──────────┬───────────────────────┘
 *               │
 *     ┌─────────┼─────────┐
 *     │         │         │
 *  CsvExporter ExcelExporter PdfExporter
 *
 * ━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━
 * 三、适用场景
 * ━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━
 * 1. 多个类有相似的操作流程，但某些步骤实现不同
 * 2. 需要控制子类的扩展点（只允许在特定步骤做变化）
 * 3. 框架设计中非常常见（Spring、JUnit、Servlet）
 *
 * ━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━
 * 四、与策略模式的区别
 * ━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━
 * 策略模式：用组合，整个算法可替换，运行时动态切换
 * 模板方法：用继承，算法骨架固定，只有部分步骤可变
 *
 * ━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━
 * 五、本示例场景 —— 数据导出系统
 * ━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━
 * 导出数据的流程是固定的：连接数据源 → 查询数据 → 格式化 → 写文件 → 清理资源。
 * 但不同导出格式（CSV、Excel、PDF）在每个步骤的具体实现不同。
 */
