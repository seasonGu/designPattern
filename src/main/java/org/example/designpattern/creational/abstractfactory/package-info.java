/**
 * 抽象工厂模式 (Abstract Factory Pattern)
 *
 * ━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━
 * 一、意图
 * ━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━
 * 提供一个接口，用于创建「一系列相关或相互依赖的对象」，
 * 而无需指定它们的具体类。
 *
 * 核心思想：工厂方法创建「一个产品」，抽象工厂创建「一族产品」。
 *          保证同一族的产品风格一致、互相兼容。
 *
 * ━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━
 * 二、UML 结构
 * ━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━
 *
 *    ┌───────────────────────┐
 *    │   UIFactory (抽象工厂) │
 *    │   + createButton()    │
 *    │   + createInput()     │
 *    │   + createDialog()    │
 *    └───────────┬───────────┘
 *                │
 *      ┌─────────┴─────────┐
 *      │                   │
 * DarkUIFactory      LightUIFactory     ← 每个工厂生产一整套产品
 *  ├─ DarkButton      ├─ LightButton
 *  ├─ DarkInput       ├─ LightInput
 *  └─ DarkDialog      └─ LightDialog
 *
 * ━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━
 * 三、与工厂方法的区别
 * ━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━
 * 工厂方法：一个工厂创建一种产品
 * 抽象工厂：一个工厂创建一族产品（多个相关产品）
 *
 * ━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━
 * 四、适用场景
 * ━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━
 * 1. 系统需要一组相关的产品，且要求它们风格一致
 * 2. 系统需要在多套产品族之间切换（如主题切换、跨平台适配）
 * 3. 需要保证产品之间的兼容性
 *
 * ━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━
 * 五、本示例场景 —— UI 主题系统
 * ━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━
 * 应用支持暗色和亮色两套主题，每套主题包含按钮、输入框、对话框。
 * 切换主题时，所有组件必须同时切换，保证风格一致。
 */
