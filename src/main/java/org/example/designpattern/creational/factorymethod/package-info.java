/**
 * 工厂方法模式 (Factory Method Pattern)
 *
 * ━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━
 * 一、意图
 * ━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━
 * 定义一个创建对象的接口，但让子类决定实例化哪个类。
 * 工厂方法将对象的创建延迟到子类。
 *
 * 核心思想：不直接 new 对象，而是通过工厂来创建，
 *          调用方只依赖抽象，不依赖具体实现类。
 *
 * ━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━
 * 二、UML 结构
 * ━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━
 *
 *         ┌──────────────────┐          ┌──────────────┐
 *         │  Product (接口)   │          │ Creator(抽象) │
 *         │  + send()        │          │ + createMsg() │ ◄── 工厂方法
 *         └──────┬───────────┘          └──────┬───────┘
 *                │                             │
 *        ┌───────┴───────┐            ┌────────┴────────┐
 *        │               │            │                 │
 *   ┌────┴────┐   ┌──────┴──┐   ┌────┴──────┐   ┌─────┴───────┐
 *   │ EmailMsg│   │ SmsMsg  │   │EmailFactory│   │ SmsFactory  │
 *   └─────────┘   └─────────┘   └───────────┘   └─────────────┘
 *
 * ━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━
 * 三、适用场景
 * ━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━
 * 1. 不知道需要创建哪个具体类时（由子类/配置决定）
 * 2. 希望将创建逻辑集中管理，便于扩展
 * 3. Spring 中大量使用：BeanFactory、FactoryBean 等
 *
 * ━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━
 * 四、与简单工厂的区别
 * ━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━
 * 简单工厂：一个工厂类 + if/switch 判断 → 违反开闭原则
 * 工厂方法：每个产品有对应的工厂子类  → 新增产品只需新增工厂，无需改已有代码
 *
 * ━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━
 * 五、本示例场景 —— 消息通知系统
 * ━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━
 * 系统需要发送不同类型的消息（邮件、短信、微信推送），
 * 使用工厂方法让每种消息类型都有自己的工厂来创建。
 */
