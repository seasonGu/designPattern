/**
 * 建造者模式 (Builder Pattern)
 *
 * ━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━
 * 一、意图
 * ━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━
 * 将一个复杂对象的构建过程与它的表示分离，
 * 使得同样的构建过程可以创建不同的表示。
 *
 * 核心思想：当一个对象有很多参数（尤其是可选参数）时，
 *          用链式调用分步设置，最后一次性构建出不可变对象。
 *
 * ━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━
 * 二、解决的问题
 * ━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━
 * 1.「构造函数爆炸」—— 参数太多，构造函数又长又难读
 * 2.「可选参数混乱」—— 不知道哪些参数是必填、哪些可选
 * 3.「对象可变性」  —— 用setter会导致对象在创建后仍可被修改
 *
 * ━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━
 * 三、两种常见形式
 * ━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━
 * 1. GoF 经典形式：Director + Builder 接口 + 多个 ConcreteBuilder
 *    → 适合同一构建过程产出不同产品
 *
 * 2. 流式 Builder（更常用）：内部静态类 + 链式调用 + build()
 *    → 解决构造函数参数爆炸问题，Lombok @Builder 就是这种
 *
 * ━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━
 * 四、本示例场景 —— HTTP 请求构建
 * ━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━
 * HTTP 请求包含：URL(必填)、方法、请求头、请求体、超时、重试次数...
 * 参数多且大部分可选，非常适合用 Builder 来构建。
 */
