/**
 * 单例模式 (Singleton Pattern)
 *
 * ========== 一、意图 ==========
 * 确保一个类只有一个实例，并提供一个全局访问点。
 *
 * ========== 二、适用场景 ==========
 * 1. 数据库连接池 —— 避免重复创建昂贵的连接资源
 * 2. 配置管理器 —— 全局统一的配置信息
 * 3. 日志记录器 —— 统一的日志输出管理
 * 4. 线程池 —— 全局共享的线程资源
 * 5. Spring Bean 默认就是单例作用域
 *
 * ========== 三、UML 类图 ==========
 *
 *  ┌─────────────────────────────┐
 *  │        Singleton            │
 *  ├─────────────────────────────┤
 *  │ - instance: Singleton       │  (静态私有实例)
 *  ├─────────────────────────────┤
 *  │ - Singleton()               │  (私有构造函数)
 *  │ + getInstance(): Singleton  │  (公共静态访问方法)
 *  └─────────────────────────────┘
 *
 * ========== 四、实现方式（由简到难） ==========
 *
 * 1. 饿汉式 (Eager)         —— 类加载时创建，线程安全，简单推荐
 * 2. 懒汉式 (Lazy)          —— 延迟创建，线程不安全（反面教材）
 * 3. 双重检查锁 (DCL)       —— 延迟创建，线程安全，性能好
 * 4. 静态内部类 (Holder)     —— 延迟创建，线程安全，推荐写法
 * 5. 枚举 (Enum)            —— 最简洁，防反射/序列化攻击，Effective Java 推荐
 *
 * ========== 五、各写法对比 ==========
 *
 *  | 写法       | 线程安全 | 懒加载 | 防反射 | 防序列化 | 推荐度 |
 *  |-----------|---------|-------|-------|---------|-------|
 *  | 饿汉式     | ✅      | ❌    | ❌    | ❌      | ★★★  |
 *  | 懒汉式     | ❌      | ✅    | ❌    | ❌      | ★☆☆  |
 *  | 双重检查锁  | ✅      | ✅    | ❌    | ❌      | ★★★  |
 *  | 静态内部类  | ✅      | ✅    | ❌    | ❌      | ★★★★ |
 *  | 枚举       | ✅      | ❌    | ✅    | ✅      | ★★★★★|
 */
