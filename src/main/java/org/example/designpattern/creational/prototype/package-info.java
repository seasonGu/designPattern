/**
 * 原型模式 (Prototype Pattern)
 *
 * ━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━
 * 一、意图
 * ━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━
 * 通过复制（克隆）一个已有的实例来创建新对象，
 * 而不是通过 new 从头构建。
 *
 * 核心思想：当创建对象的成本很高（复杂初始化、数据库查询、网络请求）时，
 *          直接克隆一个现成的对象，比从头 new 快得多。
 *
 * ━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━
 * 二、关键概念：浅拷贝 vs 深拷贝
 * ━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━
 * 浅拷贝：只复制基本类型字段，引用类型字段共享同一个对象
 * 深拷贝：递归复制所有字段，引用类型也创建新的副本
 *
 * ━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━
 * 三、适用场景
 * ━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━
 * 1. 创建对象成本高（需要从DB加载、复杂计算）
 * 2. 需要大量相似对象（游戏中的怪物、文档模板）
 * 3. 需要保存对象的快照（配合备忘录模式）
 *
 * ━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━
 * 四、本示例场景 —— 游戏角色/怪物克隆
 * ━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━
 * 游戏中有各种怪物模板（哥布林、龙、Boss），
 * 每次生成怪物时，不需要从头构建，直接克隆模板并微调属性即可。
 */
