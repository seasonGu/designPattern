/**
 * 桥接模式 (Bridge Pattern)
 *
 * ━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━
 * 一、意图
 * ━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━
 * 将抽象部分与它的实现部分分离，使它们都可以独立地变化。
 *
 * 核心思想：当一个系统有两个（或多个）独立变化的维度时，
 *          不要用继承来组合所有可能，而是用「组合」把两个维度桥接起来。
 *
 * ━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━
 * 二、解决的问题 —— 类爆炸
 * ━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━
 * 假设有 M 种消息类型 × N 种发送渠道：
 *   用继承：需要 M × N 个子类（3类型 × 3渠道 = 9个类）
 *   用桥接：只需要 M + N 个类  （3类型 + 3渠道 = 6个类）
 *
 * ━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━
 * 三、UML 结构
 * ━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━
 *
 *   抽象维度（消息类型）              实现维度（发送渠道）
 *   ┌──────────────────┐          ┌─────────────────┐
 *   │ Message (抽象)    │─ 桥接 ──►│ MessageSender   │
 *   │ - sender          │          │ (实现接口)       │
 *   │ + send()          │          │ + doSend()      │
 *   └────────┬─────────┘          └────────┬────────┘
 *            │                             │
 *   ┌────────┼────────┐          ┌─────────┼─────────┐
 *   │        │        │          │         │         │
 * Normal  Urgent  Critical    EmailSender SmsSender WechatSender
 *
 * ━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━
 * 四、适用场景
 * ━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━
 * 1. 系统有两个独立变化的维度（形状×颜色、设备×操作系统）
 * 2. 不想因为多维度组合导致类爆炸
 * 3. 需要在运行时切换实现
 *
 * ━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━
 * 五、本示例 —— 消息发送系统
 * ━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━
 * 维度A（消息类型）：普通消息、加急消息、特急消息
 * 维度B（发送渠道）：邮件、短信、微信
 * 两个维度自由组合：普通+邮件、加急+短信、特急+微信...
 */
