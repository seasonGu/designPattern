/**
 * 外观模式 (Facade Pattern)
 *
 * ━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━
 * 一、意图
 * ━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━
 * 为子系统中的一组接口提供一个统一的高层接口，
 * 使得子系统更容易使用。
 *
 * 核心思想：把复杂的内部流程藏在一个简单的方法背后。
 *          客户端只需调用一个方法，不需要知道内部涉及多少个子系统。
 *          就像酒店前台 —— 你只说"我要订房"，前台帮你搞定一切。
 *
 * ━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━
 * 二、UML 结构
 * ━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━
 *
 *                    ┌──────────────────┐
 *    客户端 ────────►│  OrderFacade     │
 *                    │  + placeOrder()  │ ◄── 简单接口
 *                    └───────┬──────────┘
 *                            │ 内部协调
 *              ┌─────────────┼─────────────┐
 *              │             │             │
 *       InventoryService  PayService   LogisticsService
 *       (库存子系统)      (支付子系统)   (物流子系统)
 *
 * ━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━
 * 三、适用场景
 * ━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━
 * 1. 简化复杂系统的调用（多个子系统协作）
 * 2. 分层架构中的 Service 层（Controller 只调 Service）
 * 3. 对第三方 SDK 做简单封装
 *
 * ━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━
 * 四、本示例场景 —— 电商下单流程
 * ━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━
 * 一个简单的下单操作，背后要协调：
 * 库存服务（检查&扣减）→ 支付服务（扣款）→ 物流服务（创建运单）→ 通知服务（发短信）
 * 外观类把这些复杂流程封装成一个 placeOrder() 方法。
 */
