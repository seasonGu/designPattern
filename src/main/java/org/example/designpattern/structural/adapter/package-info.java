/**
 * 适配器模式 (Adapter Pattern)
 *
 * ━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━
 * 一、意图
 * ━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━
 * 将一个类的接口转换成客户端期望的另一个接口，
 * 使得原本接口不兼容的类可以一起工作。
 *
 * 核心思想：就像充电器转接头 —— 你的手机是 Type-C 口，
 *          酒店只有 Lightning 线，加个转接头就能用。
 *          适配器不改变原有代码，只做「翻译」。
 *
 * ━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━
 * 二、UML 结构（对象适配器，更常用）
 * ━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━
 *
 *   客户端代码                 我方接口                   第三方（不兼容）
 *  ┌──────────┐          ┌─────────────┐
 *  │ 我方系统  │─────────►│ PayService  │           ┌─────────────────┐
 *  └──────────┘          │ + pay()     │           │ AliPaySDK       │
 *                        └──────┬──────┘           │ + sendAliPay()  │
 *                               │                  └────────┬────────┘
 *                        ┌──────┴──────┐                    │
 *                        │ AliPay      │────── 持有 ────────┘
 *                        │ Adapter     │  (把 sendAliPay 翻译成 pay)
 *                        └─────────────┘
 *
 * ━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━
 * 三、两种适配器
 * ━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━
 * 对象适配器：通过组合（持有被适配对象的引用）  ← 更灵活，推荐
 * 类适配器：  通过继承（同时继承目标接口和被适配类）← 受限于单继承
 *
 * ━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━
 * 四、适用场景
 * ━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━
 * 1. 对接第三方 SDK（接口格式、参数名、返回值不同）
 * 2. 旧系统升级（新接口兼容老接口）
 * 3. 统一多个不兼容系统的调用方式
 *
 * ━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━
 * 五、本示例场景 —— 第三方支付接口对接
 * ━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━
 * 我方系统定义了统一的 PayService 接口，
 * 但支付宝、微信、PayPal 各自的 SDK 接口完全不同。
 * 通过适配器将它们统一为 PayService 接口。
 */
