/**
 * 组合模式 (Composite Pattern)
 *
 * ━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━
 * 一、意图
 * ━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━
 * 将对象组合成树形结构来表示「部分-整体」的层次关系，
 * 使得客户端对单个对象和组合对象的使用具有一致性。
 *
 * 核心思想：叶子节点和容器节点实现同一个接口，
 *          客户端不需要区分操作的是单个对象还是整棵树。
 *
 * ━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━
 * 二、UML 结构
 * ━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━
 *
 *          ┌─────────────────────┐
 *          │ FileSystemNode      │ ◄── 统一接口
 *          │ + getName()         │
 *          │ + getSize()         │
 *          │ + print(indent)     │
 *          └─────────┬───────────┘
 *                    │
 *          ┌─────────┴─────────┐
 *          │                   │
 *    ┌─────┴─────┐    ┌───────┴───────┐
 *    │   File    │    │   Directory   │
 *    │  (叶子)   │    │  (容器)        │
 *    └───────────┘    │ - children[]  │──► 包含 FileSystemNode
 *                     │ + add()       │
 *                     │ + remove()    │
 *                     └───────────────┘
 *
 * ━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━
 * 三、适用场景
 * ━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━
 * 1. 文件系统（文件/文件夹）
 * 2. UI 组件树（组件/容器）
 * 3. 组织架构（员工/部门）
 * 4. 菜单系统（菜单项/子菜单）
 *
 * ━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━
 * 四、本示例 —— 文件系统
 * ━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━
 * File（文件）是叶子节点，Directory（文件夹）是容器节点，
 * 都实现 FileSystemNode 接口。
 * 对文件夹调用 getSize() 会递归计算所有子节点的大小。
 */
