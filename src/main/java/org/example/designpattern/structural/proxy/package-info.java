/**
 * 代理模式 (Proxy Pattern)
 *
 * ━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━
 * 一、意图
 * ━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━
 * 为其他对象提供一个代理，以控制对这个对象的访问。
 *
 * 核心思想：客户端不直接访问真实对象，而是通过代理对象间接访问。
 *          代理在调用真实对象前后可以添加额外逻辑。
 *
 * ━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━
 * 二、UML 结构
 * ━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━
 *
 *    客户端 ──────► ┌─────────────────┐
 *                  │ UserService(接口) │
 *                  └────────┬────────┘
 *                           │
 *                ┌──────────┴──────────┐
 *                │                     │
 *     ┌──────────┴──────────┐  ┌──────┴──────────┐
 *     │ UserServiceProxy    │  │ UserServiceImpl  │
 *     │ (代理)              │─►│ (真实对象)        │
 *     │ 权限校验/日志/缓存   │  │ 实际业务逻辑     │
 *     └─────────────────────┘  └─────────────────┘
 *
 * ━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━
 * 三、代理的常见类型
 * ━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━
 * 1. 保护代理：权限控制（本例演示）
 * 2. 日志代理：记录方法调用日志
 * 3. 缓存代理：缓存结果避免重复计算
 * 4. 虚拟代理：延迟加载（懒初始化）
 * 5. 远程代理：隐藏远程调用细节（如 RPC）
 *
 * ━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━
 * 四、代理 vs 装饰器 vs 适配器
 * ━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━
 * 代理：控制访问（权限、日志、缓存），接口相同
 * 装饰器：增强功能（层层叠加），接口相同
 * 适配器：转换接口（兼容不同接口），接口不同
 *
 * ━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━
 * 五、Spring 中的代理模式
 * ━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━
 * Spring AOP 的核心就是代理模式：
 *   - JDK 动态代理（基于接口）
 *   - CGLIB 代理（基于子类）
 *   - @Transactional、@Cacheable、@Async 都是通过代理实现的
 *
 * ━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━
 * 六、本示例 —— 用户服务的多种代理
 * ━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━
 * 演示三种静态代理 + JDK 动态代理：
 * 1. 权限代理：校验当前用户是否有权限
 * 2. 日志代理：记录方法调用和耗时
 * 3. 缓存代理：缓存查询结果避免重复查询
 * 4. JDK 动态代理：运行时自动生成代理类
 */
