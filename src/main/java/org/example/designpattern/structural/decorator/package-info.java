/**
 * 装饰器模式 (Decorator Pattern)
 *
 * ━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━
 * 一、意图
 * ━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━
 * 动态地给对象添加额外的职责，比继承更灵活。
 * 装饰器和被装饰对象实现相同的接口，可以层层嵌套。
 *
 * 核心思想：「套娃」—— 一层包一层，每层添加一个功能，
 *          自由组合，互不影响。
 *
 * ━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━
 * 二、UML 结构
 * ━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━
 *
 *                 ┌─────────────────┐
 *                 │  DataSource     │ ◄── 组件接口
 *                 │  + write(data)  │
 *                 │  + read(): data │
 *                 └────────┬────────┘
 *                          │
 *            ┌─────────────┼─────────────────┐
 *            │                               │
 *    ┌───────┴────────┐          ┌───────────┴──────────┐
 *    │ FileDataSource │          │ DataSourceDecorator   │ ◄── 装饰器基类
 *    │ (具体组件)      │          │ - wrappee: DataSource │──── 持有被装饰对象
 *    └────────────────┘          └───────────┬──────────┘
 *                                            │
 *                              ┌─────────────┼─────────────┐
 *                              │             │             │
 *                        Encryption    Compression     Base64
 *                        Decorator     Decorator       Decorator
 *
 * ━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━
 * 三、适用场景
 * ━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━
 * 1. 需要动态、透明地给对象添加功能
 * 2. 功能可以自由组合（A+B、A+C、A+B+C...）
 * 3. 不希望通过继承产生大量子类
 *
 * ━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━
 * 四、Java 经典案例 —— I/O 流
 * ━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━
 * new BufferedReader(new InputStreamReader(new FileInputStream("file.txt")))
 *     装饰器3(          装饰器2(              具体组件           ))
 *
 * ━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━
 * 五、本示例场景 —— 数据存储管道
 * ━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━
 * 将数据写入文件，可以自由组合：加密、压缩、Base64编码。
 * 写入时层层处理，读取时逆向还原。
 */
