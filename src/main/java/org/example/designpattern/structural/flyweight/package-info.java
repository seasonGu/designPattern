/**
 * 享元模式 (Flyweight Pattern)
 *
 * ━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━
 * 一、意图
 * ━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━
 * 运用共享技术有效地支持大量细粒度的对象。
 *
 * 核心思想：把对象的状态分成两部分：
 *   - 内部状态（Intrinsic）：不变的、可共享的（如棋子颜色、形状）
 *   - 外部状态（Extrinsic）：变化的、不可共享的（如棋子位置）
 *   共享内部状态，外部状态由客户端传入。
 *
 * ━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━
 * 二、UML 结构
 * ━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━
 *
 *   ┌────────────────┐       ┌────────────────┐
 *   │ ChessPiece     │       │ ChessFactory   │
 *   │ (享元接口)      │◄──────│ (享元工厂)      │
 *   │ + draw(x, y)   │       │ 缓存池          │
 *   └───────┬────────┘       └────────────────┘
 *           │
 *   ┌───────┴────────┐
 *   │ ChessPieceImpl │
 *   │ - color (内部)  │ ← 共享，不变
 *   │ - shape (内部)  │ ← 共享，不变
 *   │ draw(x,y)      │ ← x,y 是外部状态，由调用方传入
 *   └────────────────┘
 *
 * ━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━
 * 三、适用场景
 * ━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━
 * 1. 系统中有大量相似对象导致内存开销大
 * 2. 对象的大部分状态可以外部化
 * 3. 不依赖对象标识（共享对象不能用 == 比较）
 *
 * ━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━
 * 四、Java 经典案例
 * ━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━
 * Integer.valueOf(-128~127) → 缓存池
 * String 常量池 → "abc" == "abc" 是 true
 * Boolean.TRUE / Boolean.FALSE → 只有两个实例
 *
 * ━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━
 * 五、本示例 —— 围棋/五子棋游戏
 * ━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━
 * 棋盘上有大量棋子，但只有黑白两种颜色。
 * 不需要为每颗棋子都创建新对象，只需共享「黑子」和「白子」两个实例，
 * 位置作为外部状态传入即可。
 */
